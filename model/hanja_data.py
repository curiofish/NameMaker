def get_strokes(char):
    """한자의 획수를 반환합니다."""
    return STROKES.get(char, 0)

def get_meaning(char):
    """한자의 의미를 반환합니다."""
    return MEANINGS.get(char, '의미 없음')

def get_yinyang_score(chars):
    """음양 조화도를 점수로 반환합니다."""
    yin_count = sum(1 for char in chars if char in YINYANG and YINYANG[char] == '음')
    yang_count = sum(1 for char in chars if char in YINYANG and YINYANG[char] == '양')
    
    if yin_count == yang_count:
        return 100  # 완벽한 음양 조화
    elif abs(yin_count - yang_count) == 1:
        return 80   # 양호한 음양 조화
    else:
        return 60   # 불균형한 음양

def get_strokes_score(total_strokes):
    """획수의 길흉을 점수로 반환합니다."""
    if 20 <= total_strokes <= 30:
        return 100  # 이상적인 획수
    elif 15 <= total_strokes <= 35:
        return 80   # 양호한 획수
    else:
        return 60   # 부적절한 획수

def get_name_score(name_data):
    """이름의 종합 점수를 계산합니다."""
    chars = list(name_data['hanja'])
    yinyang_score = get_yinyang_score(chars)
    strokes_score = get_strokes_score(name_data['analysis']['strokes'])
    
    return (yinyang_score + strokes_score) / 2 